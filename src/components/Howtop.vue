      trigger: wrapper,
      start: 'top top',
      end: () => `+=${middleOf6}`,  // –∫–æ–Ω–µ—Ü –ø–∏–Ω–∞ - —Å–µ—Ä–µ–¥–∏–Ω–∞ 6-–≥–æ –ø—É–Ω–∫—Ç–∞ —Å–ø—Ä–∞–≤–∞
