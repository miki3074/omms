    // –§–∏–∫—Å–∏—Ä—É–µ–º –∑–∞–≥–æ–ª–æ–≤–æ–∫ –∏ –ø–∏–Ω–∏–º –µ–≥–æ –¥–æ —Å–µ—Ä–µ–¥–∏–Ω—ã 6-–≥–æ –ø—É–Ω–∫—Ç–∞
